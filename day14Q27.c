/* WAP to print the sum of the first n odd numbers .

input 1: 3
output 1: 9

input 2: 5
output 2: 25
*/

#include<stdio.h>
int main() {
int i,n,sum;
printf("enter the value of n :");
scanf("%d", &n);

for (i = 1; i <= n; i++) {
     sum = n * n;
}
printf("Sum of n odd numbers : %d", sum);
return 0;
}

